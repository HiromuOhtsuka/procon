int ceil(int x, int y){
  return (x % y == 0) ? x / y : x / y + 1;
}

int gcd(int x, int y){
  return y ? gcd(y, x % y) : x;
}

int lcm(int x, int y){
  return x / gcd(x, y) * y;
}

int main(){
  ios::sync_with_stdio(false);
  cin.tie(0);

  ll n, m, d;
  cin >> n >> m >> d;

  double ans;
  if(d == 0){
    ans = (1.0 / n) * (m - 1);
  }
  else{
    ans = ((2.0 * (n - d)) / (n * n)) * (m - 1);
  }

  printf("%.7lf\n", ans);

  return 0;
}
